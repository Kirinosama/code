#include <bits/stdc++.h>
using namespace std;
int n, d, a, b;
int main() {
	freopen("point.in", "r", stdin);
	freopen("point.out", "w", stdout);
	scanf("%d%d%d%d", &n, &d, &a, &b);
	for(int i = 1; i <= n; ++i) {
		int x; scanf("%d", &x);
	}
	printf("%d\n", n * a);
	fclose(stdin);
	fclose(stdout);
	return 0;
}
